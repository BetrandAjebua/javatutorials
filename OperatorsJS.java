public class OperatorsJS {
    public static void main(String[] args) {
        int num1 , num2 = 0;
        num2 = 10 ;
        num1 = 5;

//        Addition
        System.out.println(num1+num2);

//        subtraction

        System.out.println(num1-num2);


//        Multiplication

        System.out.println(num1*num2);
//        Division

        System.out.println(num1/(float)num2);
    }
}
